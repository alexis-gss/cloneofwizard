          let startRow = Math.floor(this.camera.y / this.map.tSize);
      let endRow = startRow + (this.camera.height / this.map.tSize)
          // On bloque la dernière colonne (et ligne) à 12-1 = 11 car un tableau [0, 11]
          endCol = Math.max(0, Math.min(endCol, this.map.cols - 1))
          endRow = Math.max(0, Math.min(endRow, this.map.rows - 1))
          // offset : lorsque la caméra est entre deux bloc
          let offsetX = (startCol * this.map.tSize) - this.camera.x
          let offsetY = (startRow * this.map.tSize) - this.camera.y
          this.sprites = []
          this.testColl = []
          // Création des sprites
          for(let col = startCol; col <= endCol; col++){
              for(let row = startRow; row <= endRow; row++){
                  let tile = this.map.getTile(this.layersSprite, col, row)
                  let x = Math.round((col - startCol) * this.map.tSize + offsetX)
                  let y = Math.round((row - startRow) * this.map.tSize + offsetY)
                  tile.init(x, y)
                  this.sprites.push(tile)
              }
          }
          // Création des clones s'il y en a
          for(let col = startCol ; col <= endCol ; col ++){
              for(let row = startRow ; row <= endRow ; row ++){
                  let tile = this.clone.getTile(0, col, row)
                  let x = Math.round((col - startCol) * this.clone.tSize + offsetX)
                  let y = Math.round((row - startRow) * this.clone.tSize + offsetY)
                  tile.init(x, y)
                  this.sprites.push(tile)
              }
          }
          // Création de flame s'il y en a
          for(let col = startCol ; col <= endCol ; col ++){
              for(let row = startRow ; row <= endRow ; row ++){
                  let tile = this.flame.getTile(0, col, row)
                  let x = Math.round((col - startCol) * this.flame.tSize + offsetX)
                  let y = Math.round((row - startRow) * this.flame.tSize + offsetY)
                  tile.init(x, y)
                  this.sprites.push(tile)
              }
          }
          this.testColl = []
          // Création des éléments de collision
          for(let col = startCol; col <= endCol; col++){
              for(let row = startRow; row <= endRow; row++){
                  let tile = this.map.getTile(this.layersColl, col, row)
                  if(tile != 0){
                      let x = Math.round(col * this.map.tSize)
                      let y = Math.round(row * this.map.tSize)
                      tile.init(x, y)
                      this.testColl.push(tile)
                  }
              }
          }
