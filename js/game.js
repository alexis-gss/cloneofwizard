    rectangleCollision: function(r1, r2){
      var collision = undefined
          var dw = undefined
          var dh = undefined
          var overlapX = undefined
          var overlapY = undefined
          var vx = undefined
          var vy = undefined
      // Calcul distance entre les deux rectangles
          // Attention modification le wizard est centré.
      vx = (r1.x) - (r2.x + r2.width / 2)
      vy = (r1.y) - (r2.y + r2.height / 2)
      // Disatnce min avant collision
      dw = r1.width / 2 + r2.width / 2
      dh = r1.height / 2 + r2.height / 2
      if (Math.abs(vx) < dw) {
        if(Math.abs(vy) < dh){
          // Collision !
          // On détermine de combien de pixels les éléments
          // s'interpénètrent sur chaque axe
          overlapX = dw - Math.abs(vx)
          overlapY = dh - Math.abs(vy)
          if (overlapX >= overlapY) {
            if (vy > 0) {
                          collision = "top"
                          //Place le rectangle hors de la collision
                          r1.y = r1.y + overlapY
            }
                      else {
                          if(Wizard.vy >= 0){
                              collision = "bottom"
                              //Place le rectangle hors de la collision
                              r1.y = r1.y - overlapY
                              Wizard.isOnGround = true
                              Wizard.jumping = false
                          }
            }
          }
                  else {
            if (vx > 0) {
                          collision = "left"
                          //Place le rectangle hors de la collision
                          r1.x = r1.x + overlapX
            }
                      else {
                          collision = "right"
                          //Place le rectangle hors de la collision
                          r1.x = r1.x - overlapX
            }
          }
        }
              else {
          collision = "no collision"
        }
      }
          else {
        collision = "no collision"
      }
    return collision
    }
